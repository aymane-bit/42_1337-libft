#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	j;

	j = (char)c;
	while (*s != j)
	{
		if (*s == '\0')
		{
			return (0);
		}
		s++;
	}
	return ((char *)s);
}
