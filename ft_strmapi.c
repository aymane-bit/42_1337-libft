#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*new_str;
	int		str_len;
	int		i;

	if (!s)
		return (NULL);
	str_len = ft_strlen(s);
	i = 0;
	new_str = (char *)malloc(sizeof(char) * str_len + 1);
	if (!new_str)
		return (NULL);
	while (s[i])
	{
		new_str[i] = f(i, s[i]);
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
